// translations.js  (يُحمّل قبل nav.js وكل سكربتات الصفحات)
(function () {
  // helper: جيب القيمة من جدول الترجمات
  function t(lang, key) {
    if (!window.translations || !translations[lang]) return null;
    const dict = translations[lang];
    if (dict[key] != null) return dict[key];
    // حاول تبديل - و _ إذا في اختلاف كتابة بالمفاتيح
    const swapped = key.includes('_') ? key.replaceAll('_', '-') : key.replaceAll('-', '_');
    return dict[swapped] != null ? dict[swapped] : null;
  }

  // المطبّق الرئيسي للترجمة على الـ DOM
  function applyTranslations(lang) {
    // نصوص داخلية
    document.querySelectorAll('[data-translate],[data-translate-key]').forEach(el => {
      const key = el.getAttribute('data-translate') || el.getAttribute('data-translate-key');
      const val = t(lang, key);
      if (val != null) el.innerHTML = val; // استخدم innerHTML عشان الspan داخل النص يضل شغّال
    });

    // placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
      const key = el.getAttribute('data-translate-placeholder');
      const val = t(lang, key);
      if (val != null) el.setAttribute('placeholder', val);
    });
  }

  // كشّف دالة عالميّة لو بدك تستدعيها يدويًا
  window.i18n = { apply: applyTranslations };

  // 1) طبّق عند فتح الصفحة حسب اللغة المخزّنة
  document.addEventListener('DOMContentLoaded', () => {
    const lang = localStorage.getItem('language') || 'en';
    applyTranslations(lang);
  });

  // 2) طبّق كل ما تتغير اللغة (nav.js بيعمل dispatch للـ languagechange)
  window.addEventListener('languagechange', (e) => {
    const lang = (e && e.detail && e.detail.lang) || (document.documentElement.getAttribute('lang') || 'en');
    applyTranslations(lang);
  });
})();
